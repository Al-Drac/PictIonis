package com.example.aldrac.pictionis;

public class PointP {
    private int x;
    private int y;

    public PointP() {
    }

    public PointP(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
